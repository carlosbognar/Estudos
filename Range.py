# Utilização  do RANGE
# Exemplo: range(INICIO, FIM)
# Retorna uma lista de valores a partir de INICIO (inclusive) até FIM (exclusive)

a = list(range(0, 5))
print(a)

# Utilização  do RANGE com STEP
# Exemplo: range(INICIO, FIM [, STEP])
# Retorna uma lista de valores a partir de INICIO (inclusive) até FIM (exclusive) by STEP

b = list(range(0, 10, 2))
print(b)

# Utilização  do RANGE somente com FIM
# Exemplo: range(FIM)
# Retorna uma lista de valores a partir de 0 (inclusive) até FIM (exclusive)

c = list(range(10))
print(c)