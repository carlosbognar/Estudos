# Para criar uma Tupla com um único valor, utilize a virgula no final

a = (123)    # a variável a é do tipo inteiro, não uma tupla
print(type(a))


b = (123, )    # Observe a virgula no final. Agora, b é uma tupla.
print(type(b)) # A função builtin type() permite conhecermos o typo de qualquer variável.