#########################################################################
# O método STARTSWITH verifica se o string começa com um determinado
# texto (ou substring)
########################################################################
s1 = 'Eu gosto de programar em Python'

if s1.startswith("Eu "):
    print("Sim")
else:
    print("Nao")


#########################################################################
# O método ENDSWITH verifica se o string termina com um determinado
# texto (ou substring)
########################################################################
s2 = 'Eu gosto de programar em Python'

if s2.endswith("thon"):
    print("Sim")
else:
    print("Nao")
